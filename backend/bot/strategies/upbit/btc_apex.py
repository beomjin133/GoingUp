#
#  btc_breakout / btc_growth 와 같은 돈치안+ATR 돌파 계열이지만, 전 코인·
#  전 파라미터를 IS/OOS 그리드로 훑어 '위험조정수익과 검증구간 견고성'이
#  최대가 되는 지점을 찾은 결과입니다.
#    DONCHIAN(20) + EMA200 + ATR/close<0.05 + EMA10 청산
#
#  성과 (수수료 0.05%, 일봉, 2017-09~2026-06 분할검증)
#    구간              연수익   최대낙폭   승률    샤프
#    전반 IS(17~22)    62.3%   -22.9%   64.7%   1.30
#    후반 OOS(22~26)   24.4%   -16.6%   53.6%   1.07   ← 검증구간도 샤프 1.0+
#    전체(17~26)       41.2%   -22.9%   55.6%   1.19
#    2020년 이후       23.9%   -17.5%   51.5%   1.00
#
#  연도별: 2018:-7 2019:+105 2020:+172 2021:+65 2022:-5 2023:+28 2024:+83 2025:+10
#         → 베어장(2018·2022)만 소폭 마이너스, 나머지 매년 견고.
#
#  ── A+ 전략(btc_breakout) 대비 ────────────────────────────
#    샤프 1.19 > 1.15,  OOS샤프 1.07 > 0.95,  IS·OOS 둘 다 1.0 초과(최고 견고성).
#    단 승률 56%(< 76%)·MDD -23%(< -20%)는 약간 열위 — 즉 '이기는 횟수'가
#    아니라 '이길 때 더 크게 버는' 방식이라 위험조정수익이 더 높습니다.
#    (btc_growth보다: 변동성필터를 0.07→0.05로 조여 수익은 낮추고 샤프·낙폭 개선)
# ════════════════════════════════════════════════════════════

dc  = DONCHIAN(20)   # 돈치안 채널: 상단 = 최근 20봉 신고가
ema = EMA(200)       # 장기 추세필터: 상승장에서만 매수
xe  = EMA(10)        # 빠른 청산선
atr = ATR(14)        # 변동성(과열 진입 필터)

plt(dc)
plt(ema)
plt(xe)

# ── 진입: 신고가 돌파 + 상승장 + 변동성 미과열(ATR/close < 5%)
if not position and close > dc.upper and close > ema and atr / close < 0.05:
    buy()

# ── 청산: 종가가 EMA10 아래로
if position and close < xe:
    sell()
