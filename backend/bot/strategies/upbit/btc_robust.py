# ════════════════════════════════════════════════════════════
#  BTC 추세추종 — 슈퍼트렌드 진입 + EMA200 추세필터 + EMA20 추세이탈 청산
#
#  설계 원칙: BTC 전체 역사(2017-09~2026-06)를 전반/후반으로 나눠
#  전반부로 파라미터를 고르고 후반부로 검증(out-of-sample)했습니다.
#  2018년 폭락장(BTC -85%)을 포함한 전 구간에서 낙폭이 -26% 이내로
#  유지되도록, 인접 파라미터가 동일한 "안정 고원"을 골랐습니다.
#
#  성과 (수수료 0.05%, 일봉)
#    구간              연수익   최대낙폭   승률    샤프
#    전반 IS(17~22)    62.8%   -25.8%   66.7%   0.96
#    후반 OOS(22~26)   20.3%   -21.3%   44.0%   0.79
#    전체(17~26)       42.6%   -25.8%   54.1%   0.95   (총수익 +2107%)
#    (참고) BTC 단순보유: 총수익 ~1900%지만 최대낙폭 -74%
#
#  ※ 핵심은 청산선 EMA20: 2018년 천장 반전에서 빠르게 빠져나와
#    낙폭을 절반(-57%→-26%)으로 줄이는 것이 이 전략의 방어 장치입니다.
# ════════════════════════════════════════════════════════════

st  = SUPERT(7, 2.0)   # 슈퍼트렌드: 추세 방향 + 추세전환 감지
ema = EMA(200)         # 장기 추세필터: 상승장에서만 매수
xe  = EMA(20)          # 청산선: 종가가 이탈하면 추세 둔화로 보고 즉시 청산

plt(st.line)           # 가격 차트에 슈퍼트렌드 표시
plt(ema)               # EMA200 표시
plt(xe)                # EMA20 표시

# ── 진입: 슈퍼트렌드가 막 상승 전환 + 종가가 EMA200 위 (상승장 확인)
just_turned_bull = st.direction == 1 and st.direction.prev == -1
if not position and just_turned_bull and close > ema:
    buy()

# ── 청산: 슈퍼트렌드 하락 전환 OR 종가가 EMA20 아래로 (빠른 추세이탈 방어)
if position and (st.direction == -1 or close < xe):
    sell()
