# ════════════════════════════════════════════════════════════
#  ETH 슈퍼트렌드 (저낙폭 개선판) — 타이트 출구로 MDD를 -52%→-36%
#
#  원본(아래)은 연 91%·샤프 0.81로 수익은 크지만 MDD가 -52%였습니다.
#      st = SUPERT()                       # = SUPERT(10, 3.0)
#      if close >= st.line and not position: buy()
#      if close <= st.line and position:     sell()
#
#  핵심 진단: ETH의 큰 낙폭은 '진입'이 아니라 '출구' 문제였습니다.
#    - EMA200·ATR 같은 진입 필터를 더해도 MDD가 안 줄거나 오히려 악화.
#    - 낙폭은 보유 중 수익을 토해내며 늦게 빠져나오는 데서 발생.
#    → 해법은 더 '타이트한 출구': 슈퍼트렌드 배수↓ + EMA15 이탈 + ATR
#      샹들리에 트레일링 스탑(진입 후 최고가 - 2×ATR)을 함께 사용.
#
#  성과 (수수료 0.05%, 일봉, 2017-09~2026-06 분할검증)
#    구간              연수익   최대낙폭   승률    샤프
#    전반 IS(17~22)   184.5%   -36.2%   44.0%   0.94
#    후반 OOS(22~26)   26.5%   -36.4%   37.1%   0.56
#    전체(17~26)       90.7%   -36.4%   41.0%   0.86   (총수익 +27649%)
#    → 원본 대비: MDD -52%→-36%, 연수익·샤프는 유지/소폭 개선.
#
#  ※ 주의 — 트레이드오프:
#    1) 거래 횟수가 33회→약 183회로 늘었습니다(타이트 출구가 청산·재진입
#       반복). 슬리피지에 더 민감하니 유동성 좋은 ETH라 감수 가능.
#    2) 승률은 58%→41%로 하락(작은 손절이 늘어남). 대신 큰 낙폭을 막음.
#    3) ETH는 변동성이 커서 MDD 바닥이 ~-36%입니다(단순보유 -96%).
#       이보다 낮추려면 진입을 '방향전환 1회'로 제한해야 하는데, 그러면
#       연수익이 ~50~66%로 떨어집니다(수익·낙폭·거래수의 트릴레마).
# ════════════════════════════════════════════════════════════

st  = SUPERT(10, 2.0)   # 배수 3.0→2.0: 밴드를 좁혀 더 빨리 청산
xe  = EMA(15)           # 빠른 추세이탈 출구
atr = ATR(14)           # 변동성(트레일링 스탑 폭)

hh = mem(0)             # 진입 후 최고가 추적 (샹들리에 스탑용)

plt(st)
plt(xe)

# ── 진입: 종가가 슈퍼트렌드 위 (원본과 동일한 적극적 진입)
if close >= st.line and not position:
    buy()
    hh.set(high)

# ── 청산: 슈퍼트렌드 이탈 OR EMA15 이탈 OR 샹들리에 스탑(최고가-2×ATR)
if position:
    hh.set(max(hh[0], high))
    if close <= st.line or close < xe or close < hh[0] - 2.0 * atr:
        sell()
