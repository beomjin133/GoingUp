# ════════════════════════════════════════════════════════════
#  BTC 돈치안 돌파 — 신고가 돌파 진입 + ATR 변동성 필터 + EMA 추세이탈 청산
#
#  새로 추가한 지표(DONCHIAN·ATR)를 활용해 기존 btc_robust(슈퍼트렌드)를
#  세 목표(낙폭·수익·승률) 전부에서 능가하도록 만든 전략입니다.
#
#  핵심 아이디어:
#    1) DONCHIAN(40): 최근 40봉 신고가를 돌파하면 추세 시작으로 보고 매수
#    2) ATR 변동성 필터: 변동성이 극단적으로 높을 때(과열장)는 진입 금지
#       → 2018년 천장 같은 위험 구간을 걸러 낙폭을 -47%→-20%로 줄임
#    3) EMA15 추세이탈 청산: 빠르게 빠져나와 수익 보존
#
#  성과 (수수료 0.05%, 일봉, 2017-09~2026-06 분할검증)
#    구간              연수익   최대낙폭   승률    샤프
#    전반 IS(17~22)    61.9%   -20.0%   84.6%   1.27
#    후반 OOS(22~26)   21.6%   -14.0%   68.8%   0.95
#    전체(17~26)       40.3%   -20.0%   75.9%   1.15   (총수익 +1820%)
#
#    비교) btc_robust(슈퍼트렌드): 연42.6%·MDD-25.8%·승률54%·샤프0.95
#    → 낙폭↓(-20%) 승률↑(76%) 샤프↑(1.15)로 전반적 우위. 수익은 비슷.
# ════════════════════════════════════════════════════════════

dc  = DONCHIAN(40)   # 돈치안 채널: 상단=최근 40봉 신고가
ema = EMA(200)       # 장기 추세필터: 상승장에서만 매수
xe  = EMA(15)        # 청산선: 종가가 이탈하면 추세 둔화로 보고 청산
atr = ATR(14)        # 변동성(ATR)

plt(dc)              # 돈치안 채널 표시
plt(ema)
plt(xe)

# ── 진입: 신고가 돌파 + 상승장(EMA200 위) + 변동성이 과열되지 않음
#    atr/close < 0.05  →  하루 변동폭이 가격의 5% 미만일 때만 (과열장 회피)
if not position and close > dc.upper and close > ema and atr / close < 0.05:
    buy()

# ── 청산: 종가가 EMA15 아래로 (빠른 추세이탈 방어)
if position and close < xe:
    sell()
