# ════════════════════════════════════════════════════════════
#  TRX(트론) 추세추종 — EMA 골든크로스 + EMA200 추세필터
#
#  설계 원칙: TRX 전체 역사(2018-04~2026-06)를 전반/후반으로 나눠
#  전반부로 파라미터를 고르고 후반부로 검증(out-of-sample)했습니다.
#
#  ※ 왜 BTC와 다른 전략인가:
#    TRX는 슈퍼트렌드 같은 정교한 추세전략이 전혀 통하지 않았습니다
#    (모든 파라미터에서 IS는 그럴듯해도 OOS 샤프가 0 근처로 붕괴).
#    TRX는 2020·2021·2023·2024년에 큰 폭으로 추세 상승하는 "버스트형"
#    이라, 오히려 단순한 EMA 골든크로스가 긴 추세를 안정적으로 잡습니다.
#    EMA200 필터를 더하면 하락장 진입을 걸러 승률이 크게 오릅니다.
#
#  성과 (수수료 0.05%, 일봉)
#    구간              연수익   최대낙폭   승률    샤프
#    전반 IS(18~22)    53.3%   -52.6%   71.4%   0.47
#    후반 OOS(22~26)   33.4%   -45.9%   55.6%   0.40
#    전체(18~26)       40.6%   -52.6%   56.2%   0.42   (총수익 +1530%)
#    (참고) TRX 단순보유: 총수익 1080%지만 최대낙폭 -88.9%
#
#  ※ 솔직한 한계: TRX는 BTC보다 훨씬 변동성이 커서, 최적 전략도
#    낙폭 ~-50%·샤프 0.42 수준입니다(BTC는 -26%·0.95). 그래도 단순
#    보유 대비 낙폭을 -89%→-53%로 줄이면서 총수익은 더 높습니다.
# ════════════════════════════════════════════════════════════

fast = EMA(15)    # 단기 이동평균
slow = EMA(50)    # 중기 이동평균
ema  = EMA(200)   # 장기 추세필터: 상승장에서만 매수

plt(fast)
plt(slow)
plt(ema)

# ── 진입: 단기선이 중기선을 상향 돌파(골든크로스) + 종가가 EMA200 위
if not position and crossover(fast, slow) and close > ema:
    buy()

# ── 청산: 단기선이 중기선을 하향 돌파(데드크로스)
if position and crossunder(fast, slow):
    sell()
