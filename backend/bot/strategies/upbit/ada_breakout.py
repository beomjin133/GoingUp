#  ADA 돈치안 돌파 — 신고가 돌파 진입 + ATR 변동성 필터 + EMA 추세이탈 청산
#
#  ADA 전체 역사(2017-10~2026-06)를 전반/후반으로 나눠 검증.
#  돈치안+ATR·슈퍼트렌드·EMA크로스·샹들리에를 모두 비교한 결과,
#  돈치안 돌파가 수익·승률·안정성 모두 가장 우수했습니다.
#  (샹들리에는 승률 80%였지만 거래 10건으로 표본이 얇고 OOS 샤프 하락)
#
#  핵심 아이디어:
#    1) DONCHIAN(20): 최근 20봉 신고가를 돌파하면 추세 시작으로 보고 매수
#    2) ATR 변동성 필터(atr/close<0.06): 과열장 진입을 걸러 낙폭 억제
#       (ADX·EMA크로스 등은 모두 OOS에서 무너져 제외)
#    3) EMA20 추세이탈 청산
#
#  성과 (수수료 0.05%, 일봉, 2017-10~2026-06 분할검증)
#    구간              연수익   최대낙폭   승률    샤프
#    전반 IS(17~22)    53.2%   -21.3%   100%*   0.92   (*표본 6건)
#    후반 OOS(22~26)   47.2%   -25.9%   50.0%   0.93   ← 연수익·샤프 거의 동일
#    전체(17~26)       51.0%   -25.9%   76.9%   0.94   (총수익 +3477%)
#    (참고) ADA 단순보유: 총수익 979%지만 최대낙폭 -98.3%
#
#  ※ IS/OOS 연수익(53→47%)·샤프(0.92/0.93)가 거의 같아 과적합이 아님.
#    단순보유 대비 낙폭을 -98%→-26%로 크게 줄였습니다.
# ════════════════════════════════════════════════════════════

dc  = DONCHIAN(20)   # 돈치안 채널: 상단=최근 20봉 신고가
ema = EMA(200)       # 장기 추세필터: 상승장에서만 매수
xe  = EMA(20)        # 청산선: 종가가 이탈하면 추세 둔화로 보고 청산
atr = ATR(14)        # 변동성(ATR)

plt(dc)
plt(ema)
plt(xe)

# ── 진입: 신고가 돌파 + 상승장(EMA200 위) + 변동성 미과열(ATR/close<6%)
if not position and close > dc.upper and close > ema and atr / close < 0.06:
    buy()

# ── 청산: 종가가 EMA20 아래로 (추세이탈 방어)
if position and close < xe:
    sell()
