#  BTC 고수익형 돈치안 돌파 — 연수익 최우선 (그다음 MDD, 그다음 승률)
#
#  btc_breakout과 같은 돈치안+ATR 패밀리지만, 우선순위를 '연수익'에 두고
#  재최적화했습니다: 돌파 기준을 짧게(DONCHIAN 20), 변동성 필터를 약간
#  완화(ATR<7%), 출구를 빠르게(EMA10) → 상승 추세를 더 많이/오래 포착.
#
#  ※ 과적합·연도편중 방지:
#    - 전체 역사(2017-09~2026-06)를 전반/후반으로 나눠 검증.
#    - dn 15~25 / xn 8~15 / cap 0.07~0.08 전 구간이 연 49~58%로 안정 고원
#      (cap 0.10은 MDD -44%로 붕괴 → 경계 밖). (20,10)이 그 중 MDD 최저.
#    - '한 해 의존'이 아님: 아래 연도별 수익 참고.
#
#  연도별 수익률 (완전한 해, 수수료 0.05% 일봉)
#    2018:-10  2019:+80  2020:+172  2021:+113
#    2022: -5  2023:+28  2024: +83  2025:+10
#    → 2018·2022(역대급 베어장)만 소폭 마이너스(대부분 현금 회피),
#      나머지 모든 해가 강하게 플러스. 여러 해가 독립적으로 기여.
#
#  성과 (수수료 0.05%, 일봉, 분할검증)
#    구간              연수익   최대낙폭   승률
#    전반 IS(17~22)    99%      -27%     61%
#    후반 OOS(22~26)   24%      -17%     54%   ← 검증구간도 견고히 플러스
#    전체(17~26)       57%      -27%     55%
#    (참고) BTC 단순보유: 최대낙폭 -74%
#
#  ※ IS 99% vs OOS 24%는 2017~2021 시장이 이후보다 훨씬 폭발적이었던
#    탓(전략 결함 아님). 현실적 기대 연수익은 OOS 기준 ~24% 입니다.
# ════════════════════════════════════════════════════════════

dc  = DONCHIAN(20)   # 돈치안 상단 = 최근 20봉 신고가 (짧게 → 추세 조기 포착)
ema = EMA(200)       # 장기 추세필터: 상승장에서만 매수 (베어장 회피)
xe  = EMA(10)        # 빠른 청산선: 수익 보존 + 추세이탈 방어
atr = ATR(14)        # 변동성(ATR)

plt(dc)
plt(ema)
plt(xe)

# ── 진입: 신고가 돌파 + 상승장(EMA200 위) + 변동성 미과열(ATR/close<7%)
if not position and close > dc.upper and close > ema and atr / close < 0.07:
    buy()

# ── 청산: 종가가 EMA10 아래로
if position and close < xe:
    sell()
