# ════════════════════════════════════════════════════════════
#  BTC 엘리어트 파동(근사) — ZigZag 스윙 저점 매수 + ATR 필터 + EMA 청산
#
#  엘리어트 파동을 "객관적·repaint 없는" 형태로 근사한 전략입니다.
#  순수 엘리어트는 주관적이라 지표화가 어렵지만(분석가마다 카운트가 다름),
#  그 뼈대인 '스윙 피벗(파동의 고점·저점)'은 ZigZag로 정량화할 수 있습니다.
#
#  ZIGZAG(0.08) = 가격이 직전 극값에서 8% 역행해야 비로소 피벗을 '확정'.
#    → 미확정 피벗은 절대 노출하지 않아 미래참조(look-ahead)가 없음.
#    → zz.dir 부호 전환(-1→+1) = 하락 파동이 끝나고 새 상승 파동 시작
#      = 엘리어트의 '파동 바닥(2·4파 끝)'을 보수적으로 포착.
#
#  핵심 로직:
#    1) 상승 추세(close>EMA200) 안에서
#    2) 스윙 저점이 막 '확정'되면(새 상승 파동 시작) 매수
#    3) ATR 변동성 필터: 과열장(atr/close>=5%)에서는 진입 금지
#    4) 종가가 EMA15 아래로 이탈하면 청산
#
#  성과 (수수료 0.05%, 일봉, 2017-09~2026-06 분할검증)
#    구간              연수익   최대낙폭   승률    샤프
#    전반 IS(17~22)    57.5%   -19.0%   47.4%   1.20
#    후반 OOS(22~26)   23.3%   -15.3%   37.5%   0.94   ← 검증구간도 견고
#    전체(17~26)       39.7%   -19.0%   43.6%   1.12   (총수익 +1747%)
#
#  ※ 솔직한 평가: 위험조정수익(샤프 1.12)은 btc_breakout(1.15)과 거의
#    동급이지만 승률은 낮습니다(44% vs 76%). 엘리어트를 정량화하면
#    결국 '스윙 추종'이 되어 돌파 전략과 비슷해진다는 것을 보여줍니다.
#    여기서도 성과의 핵심은 ATR 필터(MDD -54%→-19%)였습니다.
# ════════════════════════════════════════════════════════════

zz  = ZIGZAG(0.08)   # 스윙 피벗(파동 고/저점) — 8% 역행 시 확정
ema = EMA(200)       # 장기 추세필터: 상승 파동 국면에서만 매수
xe  = EMA(15)        # 청산선
atr = ATR(14)        # 변동성(ATR)

plt(zz.pivot)        # 확정된 파동 피벗 표시
plt(ema)
plt(xe)

# ── 진입: 새 상승 파동 시작(스윙 저점 확정) + 상승추세 + 변동성 미과열
bottom = zz.dir == 1 and zz.dir.prev == -1   # 하락→상승 전환 = 파동 바닥 확정
if not position and bottom and close > ema and atr / close < 0.05:
    buy()

# ── 청산: 종가가 EMA15 아래로 (파동 둔화·추세이탈 방어)
if position and close < xe:
    sell()
