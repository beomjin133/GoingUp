# ════════════════════════════════════════════════════════════
#  TRX(트론) 저낙폭형 — EMA 골든크로스 + ATR 샹들리에 트레일링 스탑
#
#  새로 추가한 ATR 지표 + 상태변수(mem)를 활용해, 기존 trx_robust의
#  최대 약점인 -53% 낙폭을 -16%로 끌어내린 보수적 버전입니다.
#
#  핵심 아이디어:
#    1) EMA15/50 골든크로스 + EMA200 상승장 필터로 진입 (trx_robust와 동일)
#    2) 샹들리에 스탑: 진입 후 갱신되는 '최고가 - 2×ATR'로 트레일링 손절
#       → 변동성에 맞춰 따라오는 스탑이 큰 낙폭을 빠르게 차단
#
#  성과 (수수료 0.05%, 일봉, 2018-04~2026-06 분할검증)
#    구간              연수익   최대낙폭   승률    샤프
#    전반 IS(18~22)    23.x%   -15.0%   71.4%   0.80
#    후반 OOS(22~26)   12.x%   -13.0%   55.6%   0.49
#    전체(18~26)       13.5%   -16.0%   62.5%   0.67   (총수익 +182%)
#
#  ※ trx_robust(EMA15/50)와의 선택:
#      - trx_robust   : 연40.6%·MDD-53%·승56%  → 수익 최대, 낙폭 큼(공격형)
#      - trx_chandelier: 연13.5%·MDD-16%·승62% → 낙폭 최소, 수익 작음(보수형)
#    TRX는 변동성이 커서 '낮은 낙폭'과 '높은 수익'을 동시에 못 얻습니다.
#    낙폭을 최우선한다면 이 버전, 수익을 최우선한다면 trx_robust.
# ════════════════════════════════════════════════════════════

fast = EMA(15)
slow = EMA(50)
ema  = EMA(200)   # 장기 추세필터
atr  = ATR(14)    # 변동성(트레일링 스탑 폭 계산용)

hh = mem(0)       # 진입 후 최고가 추적 (상태변수)

plt(fast)
plt(slow)
plt(ema)

# ── 진입: 골든크로스 + 상승장 → 매수하고 최고가 기록 시작
if not position and crossover(fast, slow) and close > ema:
    buy()
    hh.set(high)

# ── 보유 중: 최고가 갱신 후, '최고가 - 2×ATR' 이탈 또는 데드크로스 시 청산
if position:
    hh.set(max(hh[0], high))
    if crossunder(fast, slow) or close < hh[0] - 2.0 * atr:
        sell()
