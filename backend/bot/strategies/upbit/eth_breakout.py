#  ETH 돈치안 돌파 — 신고가 돌파 진입 + ATR 변동성 필터 + EMA 추세이탈 청산
#
#  BTC와 같은 돈치안+ATR 패밀리지만, ETH에 맞춰 분할검증으로 재최적화
#  (ETH 전체 역사 2017-09~2026-06을 전반/후반으로 나눠 검증).
#  여러 전략을 비교한 결과, 돈치안 돌파가 승률·안정성 모두 가장 우수했습니다
#  (EMA 크로스는 OOS 붕괴, 슈퍼트렌드는 파라미터가 불안정).
#
#  핵심 아이디어:
#    1) DONCHIAN(55): 최근 55봉 신고가를 돌파하면 추세 시작으로 보고 매수
#    2) ATR 변동성 필터(atr/close<0.05): 과열장 진입을 걸러 낙폭 억제
#    3) EMA10 추세이탈 청산: 빠르게 빠져나와 수익 보존 (ETH는 BTC보다
#       변동성이 커서 더 타이트한 출구가 유리)
#
#  성과 (수수료 0.05%, 일봉, 2017-09~2026-06 분할검증)
#    구간              연수익   최대낙폭   승률    샤프
#    전반 IS(17~22)    25.4%   -25.6%   66.7%   0.78
#    후반 OOS(22~26)   17.4%   -18.4%   57.1%   0.82   ← 검증구간도 견고
#    전체(17~26)       21.9%   -25.6%   61.5%   0.80   (총수익 +463%)
#    (참고) ETH 단순보유: 총수익 682%지만 최대낙폭 -95.6%
#
#  ※ 승률 최우선 설계: 전 구간 승률 57~67%로 일관. 단순보유 대비
#    낙폭을 -96%→-26%(현대 구간은 -18%)로 크게 줄였습니다.
# ════════════════════════════════════════════════════════════

dc  = DONCHIAN(55)   # 돈치안 채널: 상단=최근 55봉 신고가
ema = EMA(200)       # 장기 추세필터: 상승장에서만 매수
xe  = EMA(10)        # 청산선: 종가가 이탈하면 추세 둔화로 보고 청산
atr = ATR(14)        # 변동성(ATR)

plt(dc)
plt(ema)
plt(xe)

# ── 진입: 신고가 돌파 + 상승장(EMA200 위) + 변동성 미과열(ATR/close<5%)
if not position and close > dc.upper and close > ema and atr / close < 0.05:
    buy()

# ── 청산: 종가가 EMA10 아래로 (빠른 추세이탈 방어)
if position and close < xe:
    sell()
