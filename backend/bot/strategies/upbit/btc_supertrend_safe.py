# ════════════════════════════════════════════════════════════
#  BTC 슈퍼트렌드 (저낙폭) — eth_supertrend_safe 템플릿을 BTC에 맞춰 재최적화
#
#  구조: 슈퍼트렌드 진입(종가>=선) + 3중 출구(슈퍼트렌드 이탈 / EMA15 이탈
#        / ATR 샹들리에 트레일링 스탑). 차트를 전반/후반으로 나눠 전반부로
#        파라미터를 고르고 후반부로 검증(과적합 방지).
#
#  성과 (수수료 0.05%, 일봉, 2017-09~2026-06 분할검증)
#    구간              연수익   최대낙폭   승률    샤프
#    전반 IS(17~22)   153%     -30%     41%     1.12
#    후반 OOS(22~26)   37%     -28%     44%     0.94   ← 검증구간 견고
#    전체(17~26)       88%     -36%     42%     1.09   (거래 177회)
#
#  ※ ETH판과 달리 BTC는 추세가 매끄러워 이 템플릿에서도 샤프 1.1로
#    잘 작동합니다. 다만 종가>=선 재진입 구조라 거래가 잦습니다(churn).
#    낙폭을 더 줄이고 싶으면 btc_breakout(MDD-20%)을 쓰세요.
# ════════════════════════════════════════════════════════════

st  = SUPERT(10, 2.0)   # 슈퍼트렌드
xe  = EMA(15)           # 빠른 추세이탈 출구
atr = ATR(14)           # 변동성(트레일링 스탑 폭)

hh = mem(0)             # 진입 후 최고가 추적 (샹들리에 스탑)

plt(st)
plt(xe)

# ── 진입: 종가가 슈퍼트렌드 위
if close >= st.line and not position:
    buy()
    hh.set(high)

# ── 청산: 슈퍼트렌드 이탈 OR EMA15 이탈 OR 샹들리에(최고가 - 2.5×ATR)
if position:
    hh.set(max(hh[0], high))
    if close <= st.line or close < xe or close < hh[0] - 2.5 * atr:
        sell()
