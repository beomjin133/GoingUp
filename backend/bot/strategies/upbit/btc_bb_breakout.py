#  BTC 볼린저밴드 변동성 돌파 (현물 롱) — TradingView "Bollinger Bands
#  Break Out"(keio1989) 전략을 참고해 현물 롱 전용으로 이식·정제.
#
#  원본은 롱/숏·10배 레버리지·부분익절·트레일링에 7중 필터(BB폭·변동성·
#  HV·ADX·거래량·ARMA)를 쌓은 구조. 현물 엔진엔 숏·레버리지·부분익절이
#  없으므로 롱 핵심부만 옮기고, 과적합 방지를 위해 IS/OOS 분할검증으로
#  '실제로 OOS에 기여하는 필터 3개'만 남겼습니다.
#
#  남긴 필터 (나머지 원본 필터는 OOS 개선 없어 제외):
#    1) BB 상단 돌파         — 핵심 진입 트리거
#    2) EMA200 상승추세       — 원본 ARMA 추세필터의 대체 (OOS 0.53→0.81)
#    3) ADX 방향성(DI+>DI-)   — 상승 방향 확인
#    4) BB 폭 > 5%           — 밴드가 충분히 벌어진(변동성 확장) 돌파만
#  청산: 종가가 BB 중심선 이탈 (원본의 추세반전 청산)
#
#  성과 (수수료 0.05%, 일봉, 2017-09~2026-06 분할검증)
#    구간              연수익   최대낙폭   승률    샤프
#    전반 IS(17~22)    90.6%   -32.8%   71.4%   1.06
#    후반 OOS(22~26)   19.4%   -15.9%   54.2%   0.87   ← 검증구간 견고
#    전체(17~26)       50.8%   -32.8%   62.2%   0.99   (거래 45회)
#
#  ※ (BB 12~15 / dev 2.0 / 폭 0.04~0.06) 전 구간이 샤프 0.99~1.04로
#    안정 고원 → 과적합 아님. 깊은 -33% 낙폭은 2018 변동기에서 발생
#    (현대 구간 OOS는 -16%). 숏·레버리지가 없어 원본의 화려한 수익은
#    재현 안 되지만, 현물 기준으론 견고한 돌파 전략입니다.
# ════════════════════════════════════════════════════════════

bb  = BB(15, 2.0)    # 볼린저밴드 (.upper/.mid/.lower)
ema = EMA(200)       # 장기 추세필터 (원본 ARMA 대체)
dx  = ADX(14)        # 방향성 (.plus_di / .minus_di)

plt(bb)
plt(ema)

# ── 진입: BB 상단 돌파 + 상승추세 + 상승 방향성 + 밴드폭 충분(변동성 확장)
band_width = (bb.upper - bb.lower) / bb.mid
if (not position and close > bb.upper and close > ema
        and dx.plus_di > dx.minus_di and band_width > 0.05):
    buy()

# ── 청산: 종가가 BB 중심선 아래로 (추세반전)
if position and close < bb.mid:
    sell()
